"""Иерархия исключений приложения.

Разделение по типам позволяет обработчикам в API-слое
корректно маппить ошибки на HTTP-статусы без дублирования логики.

Три уровня ошибок:
- AppError — базовый класс для всех бизнес-ошибок приложения.
- UserError — ошибка ввода пользователя (HTTP 400/422).
- BusinessError — нарушение бизнес-правила (HTTP 409/422).
- NotFoundError — ресурс не найден (HTTP 404).
- AccessDeniedError — недостаточно прав (HTTP 403).

Системные ошибки (сеть, БД, сторонние сервисы) пробрасываются
как стандартные Python-исключения и перехватываются глобальным
обработчиком в main.py.
"""

from __future__ import annotations


class AppError(Exception):
    """Базовый класс для всех контролируемых ошибок приложения.

    Используется в обработчиках API для формирования стандартного ответа.
    """

    def __init__(self, message: str) -> None:
        """Инициализирует исключение с понятным сообщением."""
        super().__init__(message)
        self.message = message

    def __str__(self) -> str:
        """Возвращает сообщение об ошибке."""
        return self.message


class UserError(AppError):
    """Ошибка ввода пользователя.

    Маппится на HTTP 422 Unprocessable Entity.
    Используется когда данные технически корректны по формату,
    но нарушают ограничения бизнес-валидации.

    Примеры:
    - время окончания раньше времени начала;
    - дата окончания раньше рабочей даты;
    - пустое обязательное поле после нормализации.
    """


class BusinessError(AppError):
    """Нарушение бизнес-правила или конфликт состояния.

    Маппится на HTTP 409 Conflict.
    Используется когда операция логически невозможна
    в текущем состоянии системы.

    Примеры:
    - попытка запустить уже запущенный таймер;
    - перевод плана в статус, недоступный из текущего;
    - создание дублирующей записи.
    """


class NotFoundError(AppError):
    """Запрашиваемый ресурс не найден.

    Маппится на HTTP 404 Not Found.
    """


class AccessDeniedError(AppError):
    """Недостаточно прав для выполнения операции.

    Маппится на HTTP 403 Forbidden.
    Используется в сервисном слое, когда проверка прав
    происходит внутри бизнес-логики (а не на уровне роутера).
    """
